// verificando numero

function verificarnumero(num) {
    if(num > 0) {
        console.log(" o numero e positivo");
    } else if (num < 0) {
        console.log("o numero e negativo");
    } else {
        console.log("o numero e zero");
    }
}

verificarnumero(10);
verificarnumero(-6);
verificarnumero(0);